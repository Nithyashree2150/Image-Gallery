const gallery = document.getElementById("gallery");
const searchInput = document.getElementById("searchInput");
const filterBtns = document.querySelectorAll(".filter-btn");

let currentFilter = "all";
let filteredImages = [...galleryImages];

// Display Images
function displayImages(images){

    gallery.innerHTML = "";

    if(images.length===0){
        gallery.innerHTML=`
        <div class="no-result">
            <h2>No Images Found</h2>
        </div>`;
        return;
    }

    images.forEach((image,index)=>{

        const card=document.createElement("div");
        card.className="gallery-item";

        card.innerHTML=`

        <img src="${image.src}" alt="${image.title}">

        <div class="overlay">

            <h3>${image.title}</h3>

            <p>${image.category.toUpperCase()}</p>

        </div>

        `;

        card.addEventListener("click",()=>{

            currentIndex=index;

            openLightbox();

        });

        gallery.appendChild(card);

    });

}

displayImages(filteredImages);

// Category Filter

filterBtns.forEach(btn=>{

    btn.addEventListener("click",()=>{

        filterBtns.forEach(button=>button.classList.remove("active"));

        btn.classList.add("active");

        currentFilter=btn.dataset.filter;

        filterGallery();

    });

});

// Search

searchInput.addEventListener("input",()=>{

    filterGallery();

});

// Combined Search + Filter

function filterGallery(){
    console.log("Searching:", searchInput.value);

    const searchValue=searchInput.value.toLowerCase();

    filteredImages=galleryImages.filter(image=>{

        const categoryMatch=currentFilter==="all" || image.category===currentFilter;

        const searchMatch=

        image.title.toLowerCase().includes(searchValue) ||

        image.category.toLowerCase().includes(searchValue);

        return categoryMatch && searchMatch;

    });

    displayImages(filteredImages);

}
